// Javascript Loops
// for loop
for (let i = 0; i <= 5; i++) {
	console.log("Hello OneCoders");
}

// while loop
let count = 1;

while (count <= 5) {
	console.log("While Loop Count: ", count);
	count++;
}

// do-while loop
let i = 6;

do {
	console.log("Do-while Coount: ", i);
	i++;
} while (i <= 5);
